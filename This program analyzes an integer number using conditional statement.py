# Summary: This program analyzes an integer number using conditional statement

'''
    Sample Output
    Enter an integer number: 10
    number 10 is a positive number.
    number 10 is an even number.

    Enter an integer number: -10
    number -10 is a negative number.
    number -10 is an even number.

    Enter an integer number: 5
    number 5 is a positive number.
    number 5 is an odd number.

    Enter an integer number: -5
    number -5 is a negative number.
    number -5 is an odd number.
'''

'''
    ToDo:
    1. Input an integer number
    2. Print number is positive if it is greater than 0 else print it is negative
    3. Print number is even if it is divisible by 2 else print it is odd
'''


# input an integer number
num = int(input("Enter an integer number: "))

# Using a single print statement,
# print if the number is a positive number or a negative number
print("positive") if num > 0 else print("negative")

# Using a single print statement,
# print if the number is an even number or an odd number
print("even") if num % 2==0  else print("odd")

